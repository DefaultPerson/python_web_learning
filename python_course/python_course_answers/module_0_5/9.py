print('Задача 9. Прогрессивный налог 2')

salary = int(input("Введите ваш доход "))

if salary < 10000:
  print("Ваш налог", salary*0.13)
elif salary >= 10000 and salary < 50000:
  print("Ваш налог", (0.2 * (salary-10000))+(10000*0.13))
else:
  print("Ваш налог", ((0.3 * (salary-50000))+(0.2 * (50000-10000))+(10000*0.13)))
